function splitStringByNewLine(str = "") {
  return str.split(/\r?\n/);
}

function findInBoth(a, b) {
  let match;
  [...a].forEach((char) => {
    if (b.indexOf(char) !== -1) match = char;
  });
  return match;
}

function priority(char) {
  let value = char.charCodeAt(0) - 96;
  if (value <= -6) value += 58;
  return value;
}

const totalPriority = splitStringByNewLine(rucksacks)
  .map((rucksack) => [
    rucksack.slice(0, rucksack.length / 2),
    rucksack.slice(-rucksack.length / 2),
  ])
  .map((pairs) => findInBoth(...pairs))
  .map(priority)
  .reduce((sum, value) => sum + +value, 0);
console.log("# Total Priority:", totalPriority);

function groupBy(count) {
  return (groups, item) => {
    const group = groups.length ? groups.pop() : [];
    if (group.length === 0) {
      return [...groups, [item]];
    } else if (group.length < count) {
      return [...groups, [...group, item]];
    } else {
      return [...groups, group, [item]];
    }
  };
}

function filterInBoth(a, b) {
  let match = new Set();
  [...a].forEach((char) => {
    if (b.indexOf(char) !== -1) match.add(char);
  });
  return [...match];
}

